import mongoose from 'mongoose';

const clientSchema = new mongoose.Schema({
    identifiant: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    }
});

export default mongoose.model('Client', clientSchema);
